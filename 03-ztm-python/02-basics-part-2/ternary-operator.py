# ternary operator (sometimes known as conditional expression)

# another way to do conditional logic
# this is a shortcut (only works on some logic) 

# condition_if_true if condition else condition_if_else

# let's say we can only message someone if we are in their friend list:

is_friend = True
can_message = 'message allowed' if is_friend else 'not allowed to message'

print(can_message)